package ex0720;

public class Print_Star {
	public static void main(String[] args) {
		
		System.out.println("  *");
		System.out.println(" ***");
		System.out.println("*****");
		System.out.println(" ***");
		System.out.println("  *");
		
		System.out.println();
		
		System.out.print("  *\n");
		System.out.print(" ***\n");
		System.out.print("*****\n");
		System.out.print(" ***\n");
		System.out.print("  *\n");		
	}
}
